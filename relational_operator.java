public class relational_operator {
    public static void main(String[] args){
        int x=5;
        int y=2;
        boolean z;
        z=x<y;
        System.out.println(z);
        z=x>y;
        System.out.println(z);
        z=x==y;
        System.out.println(z);
        z=x!=y;
        System.out.println(z);
    }
}
